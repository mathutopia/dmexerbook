	ylim = (0, 360),
	xticks =tks,
	fontfamily = "宋",
	fillcolor = :blue,
	title = "当前工作持续的年限（EMPLOYMENT）",
	xlabel = "工作年限",
	ylabel = "人数"
)

# ╔═╡ c242eac5-14c3-48c4-a28e-f0c4ad494fb8
md"""
上面的设置是直观的， 下面稍作解释
- `st:` 序列类型（seriestype）的简写。设置为一个符号Symbol`:bar`或者字符串`"bar"`,都是可以的。后面的很多设置也是类似的。既可以用符号， 也可以用字符串。
