# ╔═╡ 47e453be-515a-4fb4-88d8-b4612c1d7d52
md"""
### 搜索异常检测模型
Julia中， 有一个专门的异常检测的包，[OutlierDetection.jl](https://outlierdetectionjl.github.io/OutlierDetection.jl/dev/documentation/installation/)。该包中的所有模型都定义了MLJ的接口， 因而可以通过使用MLJ中模型类似的方法调用异常检测模型。 一般来说， MLJ中的模型如果是Detector结尾， 代表该模型是异常检测模型。

下面的代码搜索MLJ中所有的异常检测模型。
"""

# ╔═╡ 9df6affa-4e56-4f69-baa6-07d18337819b
detectors = models("Detector")

# ╔═╡ c48b8d81-e1dd-4c5f-b7ee-4d83c2808d35
md"""
### 查看模型要求
从上面可以看到， MLJ中可以找到$(length(detectors))个异常检测模型。虽然模型很多， 但并非每个模型都适合所有的数据。在采用相应模型之前， 要看清楚模型对数据数据的要求。

对每个模型， 可以通过返回的元信息了解模型对数据的要求。比如，对k最近邻异常检测模型KNNDetector， 
```julia
name = "KNNDetector"
package_name = "OutlierDetectionNeighbors"
```
可以看到该模型的输入数据类型要求是：
```julia input_scitype = 
Union{Table{<:AbstractVector{<:Continuous}}, AbstractMatrix{<:Continuous}}
```
即模型要求的输入数据是连续值构成的向量或矩阵。 这样,如果我们要使用这个模型， 需要将特征转换为连续特征。 由于数据集中通常不是所有特征都满足这个条件， 所以我们需要选择我们要的特征。
"""

# ╔═╡ 1f1494b3-b79b-43ec-a721-f7186c254809
md"""
### 了解实验数据
本实验采用的是乳腺肿瘤（良、恶性）数据集， 数据集包含10个字段， 分别是：

| 字段                          | 含义      | 取值        |
|-----------------------------|---------|-----------|
| Class                       | 分类标签    | 2为良性，4为恶性 |
| Clump Thickness             | 肿瘤厚度    | 1~10      |
| Uniformity of Cell Size     | 细胞大小均匀性 | 1~10      |
| Uniformity of Cell Shape    | 细胞形状均匀性 | 1~10      |
| Marginal Adhesion           | 边缘粘附力   | 1~10      |
| Single Epithelial Cell Size | 单上皮细胞大小 | 1~10      |
| Bare Nuclei                 | 裸核      | 1~10      |
| Bland Chromatin             | 染色质的颜色  | 1~10      |
| Normal Nucleoli             | 核仁正常情况  | 1~10      |
| Mitoses                     | 有丝分裂情况  | 1~10      |

采用CSV.read读取数据集。
"""

# ╔═╡ 15e7074d-5062-462c-ba3e-d583c6607c50
tumour = CSV.read("data/brcancer.csv", DataFrame)

# ╔═╡ f100445b-9534-40dd-8ea6-58b9f1098d6e
md"""
可以看到， 数据集具有$(size(tumour)[1])行， $(size(tumour)[2])列。 为了后续建模方便， 我们首先将数据集中的标签信息分离出来。 这可以通过`unpack`函数实现。 分离之后， 变量`y`中存储的是每一个样本的标签信息， 而变量`X`中则是相应的特征。
"""

# ╔═╡ 533fb66f-ed9a-457c-892c-7e6f4202a1f8
y,X = unpack(tumour, ==(:Class)  )

# ╔═╡ 98eb99f7-db1b-4b0b-9e94-ab58bb7b9f6f
size(tumour)

# ╔═╡ a2da63f2-94e6-4119-a96a-05c6c856d216
scitype(X)

# ╔═╡ 27e3884b-4c70-4471-88ca-3ee334267e08
md"""
这里有一个分析的范例[csdn](https://blog.csdn.net/weixin_44615820/article/details/88841309)

"""

# ╔═╡ e26c7729-1788-4c58-b7d2-701f3e463d6a
md"""
## 任务2：模型训练
接下来， 我们训练一个异常检测模型， 我们希望异常检测模型能够识别恶性肿瘤。 下面以使用k最近邻模型（KNNDetector）为例演示过程。

### 构建模型
首先是使用@load宏加载模型并构建模型实例。
"""

# ╔═╡ c1c10c90-63df-4733-ade4-e11b73987a0a
knnd = (@load KNNDetector pkg=OutlierDetectionNeighbors verbosity=0)()

# ╔═╡ bffca820-9769-4649-9e4b-acd7085362b4
md"""
### 构建机器
"""

# ╔═╡ 4c9007f5-8f7d-4fc9-bfaf-f6a1109734d5
md"""
由于KNNDetector要求输入的数据是Continous类型。但我们准备的数据集中， 所有的值都是整数， 这在MLJ中， 会视其科学类型为Count。 为此， 需要首先对数据做科学类型转换， 这可以通过`coerce`函数实现。
"""

# ╔═╡ a823cfb5-9f02-4612-b032-0fcb9809a372
coerce!(X, Count => Continuous)

# ╔═╡ f4468c1d-834d-41a0-bc39-5996b519c189
md"""
构建机器是简单的， 只需要将数据和模型绑定就可以了。
"""

# ╔═╡ 74252a3f-7a0e-42af-83d3-6e8f5d3b8ccc
begin
knn_raw = machine(knnd, X)
end

# ╔═╡ de42bba1-ecec-4baf-8706-1b5014860496
md"""
### 划分数据集
为了评估异常检测模型的好坏， 我们将数据划分为训练集和测试集。 下面的训练只在训练集上进行。
"""

# ╔═╡ 68ccf93b-9a19-4abc-9609-d8fd9d9d58a0
trainid, testid = MLJ.partition(1:length(y), 0.7, shuffle = true, rng=MersenneTwister(1234)) 

# ╔═╡ bef9a727-76cb-45ec-916e-4b676d7e8ecf
md"""
### 训练模型
"""

# ╔═╡ 8ce3e23b-15a3-4b3a-982a-d415fb22f99b
begin
fit!(knn_raw, rows = trainid)
end

# ╔═╡ b798f5c9-9205-454b-af0c-1b7e28444b11
md"""
### 查看离群因子（得分）
接下来， 可以计算每个样本的离群因子（得分）。通常情况下， 一个离群点检测算法会计算所有样本的离群点得分。使用`transform`函数会分别返回训练集和测试集的离群点得分。
"""

# ╔═╡ fd5cfdcc-80b2-4cb9-ad1c-dfa4d87d2177
scores = MLJ.transform(knn_raw, rows = testid)

# ╔═╡ 76a20b99-a2cb-47ca-84b2-6e8618f9c732
md"""
既然已经得到每一个样本计算离群点得分。 接下来， 可以通过设置阈值等将得分在一定范围外的样本设为异常值。使用`classify_quantile`可以设置一个这样的分类器。下面将90%设置为阈值， 表示将90%的样本设为正常(normal）, 剩下的为异常(outlier)。
"""

# ╔═╡ cbfe2857-df02-414e-b1de-fdc63f12b8bd
classify = classify_quantile(0.9)

# ╔═╡ c1b00b64-14a5-40ea-be26-823a64084db7
train_c, test_c = classify(scores ) 

# ╔═╡ b74e4027-856f-4c24-9277-2a5136d19445
md"""
## 任务3： 应用异常检测模型
"""

# ╔═╡ 11e8ae60-33a9-4c52-a939-d5c97fe6e1ee
md"""
通常我们希望应用异常检测模型用于对样本进行分类。 上面虽然可以手动设置异常和正常， 但操作相对麻烦， 也不同跟已有的机器学习框架配合。 为此， 在OutlierDetection包中， 提供了两个函数`ProbabilisticDetector `和 `DeterministicDetector`, 可以将一个异常检测模型包装成一个可以用于预测的模型。下面演示其应用。

还是采用上述数据和模型， 但这次我们希望直接可以用异常检测模型构建分类模型。
"""

# ╔═╡ 78d58390-13a9-4641-97b1-909479bab675
md"""
### 构建机器
由于还采用相同的模型， 所以这里不再需要构建模型实例。 但我们构建的是分类模型， 需要将原异常检测模型包裹一层。
"""

# ╔═╡ 79610f10-7d08-48fc-98be-985ae5046720
begin
knn_probabilistic = machine(ProbabilisticDetector(knnd), X)
knn_deterministic = machine(DeterministicDetector(knnd), X)
end

# ╔═╡ b39834c7-acd2-45e3-af10-5f2bc48c4245
md"""
### 训练模型
"""

# ╔═╡ da979c4d-d06f-479f-9b47-64fe92710bed
begin
fit!(knn_probabilistic, rows=trainid)
fit!(knn_deterministic, rows=trainid)
end

# ╔═╡ 13c2b369-c553-4693-aeaa-b77f72d3a9f8
md"""
### 查看模型结果
如果使用的是概率模型， 可以通过predict函数获得每一个样本是正常和离群值的概率。
"""

# ╔═╡ 7f7af3f7-ad65-4bbc-a15c-b564079f823b
yp = predict(knn_probabilistic, rows=testid)

# ╔═╡ 27421684-7b11-4491-b45a-e07e6953b697
yn = replace(y, 2=>"normal", 4=>"outlier")

# ╔═╡ 768f91fb-7495-4367-8613-320163f21389
ynn = categorical(yn,levels = ["normal","outlier"], ordered=true)

# ╔═╡ 971f8d42-2059-4ec0-a63b-96a8a2d37b4b
auc(yp,ynn[testid])

